// ─── Helpers ──────────────────────────────────────────────────────────────────

function gone() {
    return new NextResponse(null, {
        status: 410,
        headers: { 'Cache-Control': 'public, max-age=86400' },
    });
}

function redirect301(destination) {
    return NextResponse.redirect(destination, { status: 301 });
}

// ─── Middleware ───────────────────────────────────────────────────────────────

export function middleware(request) {
    const { pathname } = request.nextUrl;
    const ua = request.headers.get('user-agent') || '';

    // Bot check — runs first on every matched request
    if (BLOCKED_BOTS.test(ua)) {
        return new NextResponse('Access Denied', { status: 403 });
    }

    if (!pathname.startsWith('/search-by-make/')) return NextResponse.next();

    // ── Pattern A: /search-by-make/:make/parts/:parts ─────────────────────────
    const matchA = pathname.match(MAKE_PARTS_RE);
    if (matchA) {
        const make = decodeURIComponent(matchA[1]).trim();
        const parts = decodeURIComponent(matchA[2]).trim();

        if (EXCLUDED_MAKES.has(make)) return gone();
        if (!VALID_MAKES.has(make.toLowerCase())) return gone();
        if (!SELECTED_PARTS_LC.has(parts.toLowerCase())) return gone();

        // 301 if URL has wrong casing (e.g. /alternator → /Alternator)
        const canonicalPart = SELECTED_PARTS.find(
            p => p.toLowerCase() === parts.toLowerCase()
        );
        if (canonicalPart && canonicalPart !== parts) {
            const url = request.nextUrl.clone();
            url.pathname = `/search-by-make/${encodeURIComponent(make)}/parts/${encodeURIComponent(canonicalPart)}`;
            return redirect301(url.toString());
        }

        return NextResponse.next();
    }

    // ── Pattern B: /search-by-make/:make/:model/:category/:subcategory ─────────
    const matchB = pathname.match(MAKE_MODEL_CAT_SUBCAT_RE);
    if (matchB) {
        const make = decodeURIComponent(matchB[1]).trim();
        const model = decodeURIComponent(matchB[2]).trim();
        const category = decodeURIComponent(matchB[3]).trim();
        const subcategory = decodeURIComponent(matchB[4]).trim();

        const makeLc = make.toLowerCase();
        const modelLc = model.toLowerCase();
        const catLc = category.toLowerCase();
        const subLc = subcategory.toLowerCase();

        const makeExcluded = EXCLUDED_MAKES.has(make);
        const makeExists = VALID_MAKES.has(makeLc);

        // Make is completely unknown (not in CarData, not excluded) → 410
        if (!makeExists && !makeExcluded) return gone();

        const subcatValid = SELECTED_PARTS_LC.has(subLc);
        const seoTrue = MODEL_INDEX[`${makeLc}|${modelLc}`] === 1;

        // shouldRender = !excluded && subcatValid && seoTrue
        const shouldRender = !makeExcluded && subcatValid && seoTrue;
        if (shouldRender) return NextResponse.next(); // ✅ 200

        // Product match saves the page even when shouldRender is false
        const productKey = `${makeLc}|${modelLc}|${catLc}|${subLc}`;
        if (PRODUCT_COMBOS.has(productKey)) return NextResponse.next(); // ✅ 200

        // Nothing saves it → 410 (covers C1–C7)
        return gone();
    }

    // ── Pattern C: /search-by-make/:make ──────────────────────────────────────
    const matchC = pathname.match(MAKE_ONLY_RE);
    if (matchC) {
        const make = decodeURIComponent(matchC[1]).trim();
        if (EXCLUDED_MAKES.has(make)) return gone();
        if (!VALID_MAKES.has(make.toLowerCase())) return gone();
        return NextResponse.next();
    }

    // ── Pattern D: /search-by-make/:make/:model ────────────────────────────────
    // Model landing page — seo flag is intentionally NOT checked.
    // These pages always render regardless of seo:true/false.
    // Only check that the make itself is valid and not excluded.
    const matchD = pathname.match(MAKE_MODEL_RE);
    if (matchD) {
        const make = decodeURIComponent(matchD[1]).trim();
        const model = decodeURIComponent(matchD[2]).trim();

        if (EXCLUDED_MAKES.has(make)) return gone();
        if (!VALID_MAKES.has(make.toLowerCase())) return gone();

        // Model must at least exist in CarData (seo value doesn't matter)
        const key = `${make.toLowerCase()}|${model.toLowerCase()}`;
        if (MODEL_INDEX[key] === undefined) return gone(); // model not in CarData at all

        return NextResponse.next(); // ✅ 200 — seo:true or seo:false, both pass
    }

    // Any other /search-by-make/* shape → 410
    return gone();
}

