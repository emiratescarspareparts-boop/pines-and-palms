// C: /search-by-make/:make  (exactly 1 segment)
const MAKE_ONLY_RE = /^\/search-by-make\/([^/]+)\/?$/;

// D: /search-by-make/:make/:model  (exactly 2 segments)
// seo flag is NOT checked here — model pages always render regardless of seo
const MAKE_MODEL_RE = /^\/search-by-make\/([^/]+)\/([^/]+)\/?$/;

// ─── Blocked bots ─────────────────────────────────────────────────────────────

